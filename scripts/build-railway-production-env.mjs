#!/usr/bin/env node
/**
 * Build railway.production.env from railway.env.example + repo-root .env secrets.
 * Uses DATABASE_URL_PROD → DATABASE_URL for Railway (local switcher pattern).
 *
 *   node scripts/build-railway-production-env.mjs
 *   node scripts/build-railway-production-env.mjs --write
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const templatePath = resolve(root, "railway.env.example");
const envPath = resolve(root, ".env");
const outPath = resolve(root, "railway.production.env");
const write = process.argv.includes("--write");

function parseEnv(text) {
  const map = new Map();
  for (const line of text.split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const eq = t.indexOf("=");
    if (eq < 1) continue;
    const key = t.slice(0, eq).trim();
    let val = t.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    map.set(key, val);
  }
  return map;
}

if (!existsSync(templatePath)) {
  console.error("Missing railway.env.example");
  process.exit(1);
}
if (!existsSync(envPath)) {
  console.error("Missing repo-root .env");
  process.exit(1);
}

const template = parseEnv(readFileSync(templatePath, "utf8"));
const local = parseEnv(readFileSync(envPath, "utf8"));

const prodDb = local.get("DATABASE_URL_PROD") ?? "";
if (!prodDb) {
  console.error("DATABASE_URL_PROD missing in .env — required for prod Railway sync.");
  process.exit(1);
}

const secretKeys = [
  "CLERK_SECRET_KEY",
  "CLERK_PUBLISHABLE_KEY",
  "INTERNAL_OPS_SECRET",
  "INTERNAL_EXEC_PATH",
];

/** Prefer repo-root .env when template still has placeholders. */
function localValueForTemplateKey(key, templateVal) {
  const fromLocal = local.get(key);
  if (!fromLocal || fromLocal.includes("REPLACE_ME")) return null;
  if (templateVal?.includes("REPLACE_ME")) return fromLocal;
  if (key.startsWith("STRIPE_") || key.startsWith("RESEND_")) return fromLocal;
  return null;
}

function shouldEmitTemplateValue(key, val) {
  if (!val || val.includes("REPLACE_ME")) return false;
  if (key.startsWith("TWILIO_")) return false;
  return true;
}

const lines = [];
lines.push("# Generated by scripts/build-railway-production-env.mjs — do not commit");
lines.push("# Sync: railway environment production && railway service link @workspace/api-server");
lines.push("#         pnpm railway:sync-env -- --prune-legacy && railway redeploy --yes");
lines.push("");

for (const [key, val] of template) {
  if (key === "DATABASE_URL") {
    lines.push(`DATABASE_URL=${prodDb}`);
    continue;
  }
  if (secretKeys.includes(key)) {
    const fromLocal = local.get(key);
    if (fromLocal) {
      lines.push(`${key}=${fromLocal}`);
      continue;
    }
  }
  const overridden = localValueForTemplateKey(key, val);
  if (overridden) {
    lines.push(`${key}=${overridden}`);
    continue;
  }
  if (shouldEmitTemplateValue(key, val)) lines.push(`${key}=${val}`);
}

for (const key of secretKeys) {
  if (template.has(key)) continue;
  const fromLocal = local.get(key);
  if (fromLocal) lines.push(`${key}=${fromLocal}`);
}

const body = `${lines.join("\n")}\n`;

if (write) {
  writeFileSync(outPath, body, "utf8");
  console.log(`Wrote ${outPath} (${lines.length} lines)`);
} else {
  console.log(body.replace(/(SECRET|KEY|PASSWORD|DATABASE_URL=postgresql:\/\/)[^\n]*/gi, (m) => {
    if (m.startsWith("DATABASE_URL=")) return "DATABASE_URL=***";
    return m.split("=")[0] + "=***";
  }));
  console.log("\nRe-run with --write to create railway.production.env");
}
